REPORT_STATE = {
    "business_question": "Is the Baja segment growing fast enough to justify dedicated product lines?",
    "executive_summary": "Baja is growing. Act now.",
    "data_findings": ["Baja grew 64% YoY", "Southwest dominates"],
    "market_findings": ["Market grew 22%", "No dedicated line exists"],
    "recommendations": [
        {"rank": 1, "action": "Launch Baja line", "rationale": "64% growth",
         "priority": "high", "timeframe": "short_term"},
    ],
    "opportunities": ["Pre-runner gear gap"],
    "data_health_score": 91,
    "errors": [],
}

SAMPLE_REPORT = """# Baja Intelligence Report

## Executive Summary
Baja is growing fast. HDE should act immediately.

## Data Findings
- Baja grew 64% year-over-year

## Strategic Recommendations
1. **Launch dedicated Baja product line** [HIGH / short term]
   Revenue growth justifies the investment.

---
*Report generated by Overland Intelligence System v1.0*"""


def make_report_llm(content=SAMPLE_REPORT):
    mock = MagicMock()
    mock.invoke.return_value = MagicMock(content=content)
    return mock


def test_report_agent_returns_markdown():
    from agents.report_agent import run_report_agent
    result = run_report_agent(dict(REPORT_STATE), make_report_llm())
    assert "report_markdown" in result
    assert len(result["report_markdown"]) > 50


def test_report_agent_status_set():
    from agents.report_agent import run_report_agent
    result = run_report_agent(dict(REPORT_STATE), make_report_llm())
    assert result["status"] == "report_complete"


def test_report_agent_strips_code_fences():
    from agents.report_agent import run_report_agent
    fenced = f"```markdown\n{SAMPLE_REPORT}\n```"
    result = run_report_agent(dict(REPORT_STATE), make_report_llm(fenced))
    assert not result["report_markdown"].startswith("```")


def test_report_agent_raises_on_llm_failure():
    from agents.report_agent import run_report_agent
    from tools.exceptions import LLMProviderError
    bad_llm = MagicMock()
    bad_llm.invoke.side_effect = Exception("LLM offline")
    with pytest.raises(LLMProviderError):
        run_report_agent(dict(REPORT_STATE), bad_llm)


