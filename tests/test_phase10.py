def test_pdf_builder_creates_file():
    from output.pdf_builder import build_pdf
    with tempfile.TemporaryDirectory() as tmpdir:
        path = os.path.join(tmpdir, "test_report.pdf")
        result = build_pdf(SAMPLE_REPORT, {}, output_path=path)
        assert os.path.exists(result)
        assert os.path.getsize(result) > 1000


def test_pdf_builder_with_charts():
    from output.pdf_builder import build_pdf
    from tools.data_tools import load_dataframe, clean_dataframe
    from tools.chart_tools import generate_all_charts
    df = clean_dataframe(load_dataframe(SAMPLE_CSV))
    charts = generate_all_charts(df)
    with tempfile.TemporaryDirectory() as tmpdir:
        path = os.path.join(tmpdir, "full_report.pdf")
        result = build_pdf(SAMPLE_REPORT, charts, output_path=path)
        assert os.path.exists(result)
        assert os.path.getsize(result) > 5000


def test_pdf_builder_returns_path_string():
    from output.pdf_builder import build_pdf
    with tempfile.TemporaryDirectory() as tmpdir:
        path = os.path.join(tmpdir, "out.pdf")
        result = build_pdf(SAMPLE_REPORT, {}, output_path=path)
        assert isinstance(result, str)
        assert result.endswith(".pdf")


def test_markdown_to_html_converts_headers():
    from output.pdf_builder import _markdown_to_html
    md = "# Title\n## Section\n### Subsection"
    html = _markdown_to_html(md)
    assert "<h1>" in html
    assert "<h2>" in html
    assert "<h3>" in html


def test_markdown_to_html_converts_bullets():
    from output.pdf_builder import _markdown_to_html
    md = "- Item one\n- Item two\n- Item three"
    html = _markdown_to_html(md)
    assert "<ul>" in html
    assert "<li>" in html
    assert html.count("<li>") == 3